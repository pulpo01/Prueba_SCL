package com.tmmas.scl.quiosco.web.VO;

public class AccesorioVO {
	
	private String codigo;
	private String serie;
	private String descripcion;
	private String cantidad;
	private String precioUnitario;
	private String total;
	private String idAccesorio;
	private String numeroKit;
	private String itbm;
	private String isc;
	
	public String getItbm() {
		return itbm;
	}
	public void setItbm(String itbm) {
		this.itbm = itbm;
	}
	public String getIsc() {
		return isc;
	}
	public void setIsc(String isc) {
		this.isc = isc;
	}
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getSerie() {
		return serie;
	}
	public void setSerie(String serie) {
		this.serie = serie;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getCantidad() {
		return cantidad;
	}
	public void setCantidad(String cantidad) {
		this.cantidad = cantidad;
	}
	public String getPrecioUnitario() {
		return precioUnitario;
	}
	public void setPrecioUnitario(String precioUnitario) {
		this.precioUnitario = precioUnitario;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getIdAccesorio() {
		return idAccesorio;
	}
	public void setIdAccesorio(String idAccesorio) {
		this.idAccesorio = idAccesorio;
	}
	public String getNumeroKit() {
		return numeroKit;
	}
	public void setNumeroKit(String numeroKit) {
		this.numeroKit = numeroKit;
	}
}
