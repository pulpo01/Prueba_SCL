package com.tmmas.scl.quiosco.web.VO;

public class KitVO {
	
	private String idKit;
	private String numeroKit;
	private String descripcionKit;
	private String precioKit;
	private String itbm;
	private String isc;
	
	public String getItbm() {
		return itbm;
	}
	public void setItbm(String itbm) {
		this.itbm = itbm;
	}
	public String getIsc() {
		return isc;
	}
	public void setIsc(String isc) {
		this.isc = isc;
	}
	public String getNumeroKit() {
		return numeroKit;
	}
	public void setNumeroKit(String numeroKit) {
		this.numeroKit = numeroKit;
	}
	public String getDescripcionKit() {
		return descripcionKit;
	}
	public void setDescripcionKit(String descripcionKit) {
		this.descripcionKit = descripcionKit;
	}
	public String getPrecioKit() {
		return precioKit;
	}
	public void setPrecioKit(String precioKit) {
		this.precioKit = precioKit;
	}
	public String getIdKit() {
		return idKit;
	}
	public void setIdKit(String idKit) {
		this.idKit = idKit;
	}
	

}
