--Baja Totales de de Pago/Resumen Consumo a Archivo Plano
--Genera El Archivo <TOTAL_DETALLE.data>
--con los totales dado el numero de secuencia

SET PAGES 0;
SET PAUSE OFF;
SET VERI OFF;
--SET HEAD OFF;
SET LINESIZE 600;
SET FEEDBACK OFF;
SET COLSEP "|";
SET TAB OFF;
SET TRIMSPOOL ON;
COLUMN NUM_SECUINFO   FORMAT 99999999
COLUMN NOM_ARCHIVO    FORMAT A250;
COLUMN NUM_CLIENTES   FORMAT 9999999999;
COLUMN TOT_LOCALES    FORMAT 999999999999999;
COLUMN TOT_INTERZONA  FORMAT 999999999999999;
COLUMN TOT_ESPECIALES FORMAT 999999999999999;
COLUMN TOT_CARRIER    FORMAT 999999999999999;
COLUMN TOT_ROAMING    FORMAT 999999999999999;

SELECT NUM_SECUINFO , 
       NOM_ARCHIVO , 
       NUM_CLIENTES , 
       TOT_LOCALES,   
       TOT_INTERZONA,
       TOT_ROAMING,
       TOT_CARRIER,
       TOT_ESPECIALES
FROM   FAD_CTLIMPRES 
WHERE  NUM_SECUINFO = &1;
QUIT;
